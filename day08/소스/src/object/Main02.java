package object;

class HelloWorld{
	String message;
	
	void sayHello() {
		System.out.println( message );
	}
	void setEng() {
		message = "Hello Java";
	}
	void setKor() {
		message = "안녕하세요, 자바";
	}
}

public class Main02 {

	public static void main(String[] args) {
		HelloWorld hello = new HelloWorld();
		
		// 메서드 호출
		hello.setKor();
		hello.sayHello();
		hello.setEng();
		hello.sayHello();
		
		hello.setKor();
		hello.sayHello();
		
	}

}













